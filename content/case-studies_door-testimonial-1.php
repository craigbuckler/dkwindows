<h2>New Door &amp; Sidelight Installation</h2>
</hgroup>

<p>Mr B from Exmouth asked DK Glass &amp; Windows to supply and fit a new composite door and sidelight.</p>

<p>We showed him a selection of replacement door brochures and he selected one from our composite range. His new door was ordered, delivered and fitted within three weeks.</p>

<h2>Installation Obstacles</h2>

<p>The job progressed smoothly but every project requires an element of problem solving. In this case, the main issue was an electrical junction box which had been fitted too close to the old door. It did not give enough room for the new sill.</p>

<p>Less experienced fitters would have cut the new sill around the box. Dave knew this would look untidy so, having discussed it with Mr B, he cut a notch in the junction box and let the sill run into it. The box was sealed to disguise the join and the overall effect was considerably better.</p>

<h2>Customer Testimonial</h2>

<p>Mr B was extremely pleased with his new door. He commented on the superior quality and the attention to detail when it was installed.</p>


<p>Mr M contacted us on behalf of his daughter, who has house in Exmouth which required new fascias, soffits, gutters and downpipes. Our quote was significantly lower than our competitors so Mr M authorised [[COMPANY]] to complete the work.</p>

<h2>Installation Obstacles</h2>

<p>The job presented several challenges but these were overcome with a little imagination and initiative. Two scaffolding towers were required: one for three sides of the house and a separate, narrower tower to fit in the limited space on the fourth side.</p>

<p>On four of the gable ends, it was necessary to cut the fascia board around every roof tile and create capping for the eaves that protruded through to the outside of the building. This was time consuming, but worth the effort as the end result looked amazing.</p>

<p>The project began in December 2010 shortly before the heavy snow fall. This postponed completion for a couple of weeks since it was too dangerous to work on scaffolding towers in icy conditions. The work was finished in early January, although we would normally recommend roofline projects during warmer months of the year.</p>

<h2>Customer Testimonial</h2>

<p>Mr M and his daughter were very pleased with the end result. The look of the house was dramatically improved and the work would provide a great looking, low maintenance solution for decades to come.</p>


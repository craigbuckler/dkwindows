<section id="bead">
<h2>Internal Bead Option</h2>

<p>All our windows require an internal bead which secures the glass in the frame while adding a unique look to the inside of the window. We normally install the decorative bead &ndash; most people&#8217;s first choice. However, you can choose from any of the 4 beads at no extra charge.</p>

<img src="[[root]]images/window-beads.jpg" width="290" height="89" alt="window beads" class="figure" />


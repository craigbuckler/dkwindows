<h2>Window Types</h2>

<p>We offer three types of window to suit any type of property: </p>

<ul>
<li>The most common type for residential properties is the &#8220;open-out casement&#8221; window.</li>
<li>High-rise appartments or business properties often require &#8220;tilt and turn&#8221; windows to maximise light and safety.</li>
<li>&#8220;Vertical sliding sash&#8221; windows are an elegant alternative which are often used in older buildings to maintain the original charm.</li>
</ul>

<img src="[[root]]images/window-types.jpg" width="290" height="137" alt="casement, tilt and turn, and sliding sash windows" class="figure" />

<h2>Secondary Windows</h2>

<p>We also install secondary glazing; a &#8216;second window&#8217; placed behind an existing one. These are often installed to reduce noise and improve energy efficiency when modern double glazing is not an option e.g. in a listed building. They can even be installed alongside modern double glazed windows in properties beside a main road, airport or railway to further reduce outside noise. Few of our competitors offer secondary glazing as an option.</p>
</section>


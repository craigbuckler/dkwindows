<section id="glass">
<h2>Glass Options</h2>

<p>All our windows use Pilkington energiKare glass. It reduces the amount of heat lost through your windows while allowing more heat from the sun in. This effect is known as &#8220;solar gain&#8221;: the windows are efficient and will save your energy costs.</p>

<img src="[[root]]images/window-glass.jpg" width="290" height="137" alt="window glass" class="figure" />

<p>Most windows need clear glass but obscure glass can be fitted to bathrooms and other rooms requiring privacy.</p>

<p>Other glass options include georgian bars and lead designs. Note that we are able to position the Georgian bars on the inside of the double glazed unit, avoiding any build-up of dirt and making cleaning easier.</p>


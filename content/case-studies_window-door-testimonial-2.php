<p>We acted quickly to quote for the job, order and fit within a short period of time. Various options were discussed with our client although the design decisions were straight forward since it was important for the property to be in-keeping with neighbouring properties. However, it was necessary to make some minor alterations in order to conform to FENSA regulations.</p>

<p>The project included a very attractive bow bay window using the existing timber canopy. We chose to insert the Georgian bar design between the glass panes rather that on the outside to permit easier cleaning.</p>

